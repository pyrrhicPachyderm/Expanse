package com.expanse.maths;

public class Plane {
	
	//Coefficients of variables in equation ax + by + cz + d= 0
	public float a = 0;
	public float b = 0;
	public float c = 0;
	public float d = 0;
	
	public Plane(){}
	
	public Plane(float a, float b, float c, float d){
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
	}
	
}
